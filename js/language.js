// Language switching function with comprehensive element handling
function setLanguage(lang) {
  // Save language preference
  localStorage.setItem("siteLanguage", lang);
  
  // Update all elements with data-key attribute
  document.querySelectorAll("[data-key]").forEach(el => {
    const key = el.getAttribute("data-key");
    if (translations[lang] && translations[lang][key]) {
      const value = translations[lang][key];
      
      // Handle different element types
      if (el.tagName === "INPUT" && el.type !== "submit" && el.type !== "button") {
        // For input fields, update placeholder
        el.placeholder = value;
      } else if (el.tagName === "TEXTAREA") {
        // For textarea, update placeholder
        el.placeholder = value;
      } else if (el.tagName === "OPTION") {
        // For select options
        el.textContent = value;
      } else if (el.tagName === "BUTTON" || (el.tagName === "INPUT" && (el.type === "submit" || el.type === "button"))) {
        // For buttons
        if (el.tagName === "INPUT") {
          el.value = value;
        } else {
          el.textContent = value;
        }
      } else {
        // For all other elements (span, p, h1-h6, a, div, etc.)
        el.textContent = value;
      }
    }
  });
  
  // Update language selector active state
  document.querySelectorAll(".language-option").forEach(opt => {
    opt.classList.remove("active");
  });
  const activeOption = document.querySelector(`[data-lang="${lang}"]`);
  if (activeOption) {
    activeOption.classList.add("active");
  }
  
  // Update page language attribute
  document.documentElement.lang = lang === "marathi" ? "mr" : "en";
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
  // Get saved language or default to Marathi
  const savedLang = localStorage.getItem("siteLanguage") || "marathi";
  
  // Apply language
  setLanguage(savedLang);
  
  // Add event listeners to language switcher buttons
  document.querySelectorAll(".language-option").forEach(option => {
    option.addEventListener("click", function(e) {
      e.preventDefault();
      const lang = this.getAttribute("data-lang");
      setLanguage(lang);
    });
  });
});
